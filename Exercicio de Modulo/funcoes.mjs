function mostraIdade(nome, idade) {
    return `A idade de ${nome} é ${idade}`;
}

function mostraCidade(nome, cidade) {
    return `A cidade de ${nome} é ${cidade}`;
}

function mostraHobby(nome, hobby) {
    return `O hobby de ${nome} é ${hobby}`;

}

function mostraCor(nome, cor) {
    return `A cor favorita de ${nome} é ${cor}`
}

function tipoDeMusica(nome, musica) {
    return `O tipo de música favorito de ${nome} é ${musica}`
}

function anoQueNasceu(nome, ano) {
    return `O ano que ${nome} nasceu foi ${ano}`
}



export { mostraIdade, mostraCidade, mostraHobby, mostraCor, tipoDeMusica, anoQueNasceu };